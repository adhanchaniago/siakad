  function set(tipe){
    if(tipe == '-1'){
      $('#edit').removeClass('btn-warning disable');
      $('#edit').addClass('btn-success');
      $('#edit').html('<i class="fa fa-save"></i> Simpan');
      $('.rektor').attr("disabled",false);
      $('#edit').attr("type",'button');
      $('#edit').attr("onClick",'set(1)');
    }else {
      $('#edit').removeClass('btn-success disable');
      $('#edit').addClass('btn-warning');
      $('#edit').html('<i class="fa fa-edit"></i> Edit');
      $('.rektor').attr("disabled",true);
      $('#edit').attr("type",'submit');
      $('#edit').attr("onClick",'set(-1)');
    }
  };
